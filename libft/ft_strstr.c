#include "libft.h"

char	*ft_strstr(char *s1, char *s2)
{
	size_t n;

	n = ft_strlen(s2);
	while (*s1)
	{
		if (!ft_memcmp(s1++, s2, n))
			return (s1 - 1);
	}
	return (0);
}
